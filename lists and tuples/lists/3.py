# list methods


l=[1,9,12,4,10,66,25,99,65,54,33,7,8,82]
print(l)
l.sort()    #used for list sorting
print(l)
l.reverse()
print(l)    #used for list reversing
l.append(100)   #used to add obj at the end of the list
print(l)
l.insert(7, 77)   #used to insert obj at the desired place
l.pop(6)        #removes selected index obj
l.remove(100)   #deletes selected obj
print(l)

